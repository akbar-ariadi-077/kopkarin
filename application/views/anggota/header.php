        function pinjaman_lainnya(str) {
            if (str == 'Lainnya') {
                document.getElementById("pinjaman_lainnya").style.display = "block";
                if (window.XMLHttpRequest) {
                    xmlhttpnopol = new XMLHttpRequest();
                } else {
                    xmlhttpnopol = new ActiveXObject("Microsoft.XMLHTTP");
                }
                xmlhttpnopol.onreadystatechange = function() {
                    if (this.readyState == 4 && this.status == 200) {
                        document.getElementById("pinjaman_lainnya").innerHTML = this.responseText;
                    }
                };

                xmlhttpnopol.open("GET", "<?= base_url(); ?>anggota/pinla", true);
                xmlhttpnopol.send();
            } else {
                document.getElementById("pinjaman_lainnya").style.display = "none";
            }
        }
    </script>

